def add_detailed_chapter_analysis(story, styles, report_data):

    story.append(
        Paragraph(
            "Detailed Chapter Analysis",
            styles["Heading"]
        )
    )

    chapters = report_data.get("chapters", [])

    if not chapters:

        story.append(
            Paragraph(
                "No chapter analysis available.",
                styles["NormalText"]
            )
        )

        add_footer(story, styles)
        story.append(PageBreak())
        return

    for chapter in chapters:

        chapter_name = chapter.get("chapter", "Unknown Chapter")
        score = chapter.get("score", 0)
        status = chapter.get("status", "PASS")
        issues = chapter.get("issues", [])

        # Skip chapters without problems
        if status == "PASS":
            continue

        # Status color
        if status == "WARNING":
            status_color = WARNING
        else:
            status_color = ERROR

        story.append(
            Paragraph(
                f"<b>{chapter_name}</b>",
                styles["SubHeading"]
            )
        )

        info_table = Table(
            [
                ["Score", f"{score}/100"],
                ["Status", status],
                ["Issues", str(len(issues))]
            ],
            colWidths=[2.2 * inch, 3.5 * inch]
        )

        info_table.setStyle(TableStyle([
            ("GRID", (0, 0), (-1, -1), 0.4, colors.grey),
            ("BACKGROUND", (0, 0), (0, -1), LIGHT),
            ("TEXTCOLOR", (0, 0), (0, -1), PRIMARY),
            ("FONTNAME", (0, 0), (0, -1), "Helvetica-Bold"),
            ("TEXTCOLOR", (1, 1), (1, 1), status_color),
            ("BOTTOMPADDING", (0, 0), (-1, -1), 8),
            ("TOPPADDING", (0, 0), (-1, -1), 8)
        ]))

        story.append(info_table)

        story.append(Spacer(1, 0.12 * inch))

        if issues:

            issue_rows = [
                ["Severity", "Rule", "Message"]
            ]

            for issue in issues:

                issue_rows.append([
                    issue.get("severity", ""),
                    issue.get("rule", issue.get("field", "")),
                    issue.get("message", "")
                ])

            issue_table = Table(
                issue_rows,
                colWidths=[
                    1.1 * inch,
                    1.5 * inch,
                    3.6 * inch
                ]
            )

            issue_table.setStyle(TableStyle([
                ("BACKGROUND", (0, 0), (-1, 0), PRIMARY),
                ("TEXTCOLOR", (0, 0), (-1, 0), colors.white),
                ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
                ("GRID", (0, 0), (-1, -1), 0.3, colors.grey),
                ("BOTTOMPADDING", (0, 0), (-1, -1), 6),
                ("TOPPADDING", (0, 0), (-1, -1), 6)
            ]))

            story.append(issue_table)

        else:

            story.append(
                Paragraph(
                    "<font color='green'>No issues found.</font>",
                    styles["NormalText"]
                )
            )

        story.append(Spacer(1, 0.35 * inch))

    add_footer(story, styles)
    story.append(PageBreak())

def add_recommendations_page(story, styles, report_data):

    story.append(
        Paragraph(
            "Recommendations",
            styles["Heading"]
        )
    )

    recommendations = report_data.get("recommendations", [])

    if not recommendations:

        story.append(
            Paragraph(
                "Congratulations! No recommendations. The EPUB meets all quality requirements.",
                styles["NormalText"]
            )
        )

    else:

        story.append(
            Paragraph(
                "The following improvements are recommended to increase the EPUB quality:",
                styles["NormalText"]
            )
        )

        story.append(Spacer(1, 0.20 * inch))

        rows = [["#", "Recommendation"]]

        for index, recommendation in enumerate(recommendations, start=1):

            rows.append([
                str(index),
                recommendation
            ])

        table = Table(
            rows,
            colWidths=[0.5 * inch, 5.8 * inch]
        )

        table.setStyle(TableStyle([

            ("BACKGROUND", (0,0), (-1,0), PRIMARY),

            ("TEXTCOLOR", (0,0), (-1,0), colors.white),

            ("FONTNAME", (0,0), (-1,0), "Helvetica-Bold"),

            ("GRID", (0,0), (-1,-1), 0.3, colors.grey),

            ("BACKGROUND", (0,1), (0,-1), LIGHT),

            ("FONTNAME", (0,1), (0,-1), "Helvetica-Bold"),

            ("VALIGN", (0,0), (-1,-1), "TOP"),

            ("BOTTOMPADDING", (0,0), (-1,-1), 8),

            ("TOPPADDING", (0,0), (-1,-1), 8)

        ]))

        story.append(table)

    story.append(Spacer(1,0.35*inch))

    story.append(
        Paragraph(
            "<b>Final Remarks</b>",
            styles["SubHeading"]
        )
    )

    score = report_data.get("summary", {}).get("overall_score", 0)

    if score >= 90:

        remarks = (
            "Excellent EPUB. It is production-ready and follows most quality guidelines."
        )

    elif score >= 80:

        remarks = (
            "Good EPUB with only minor improvements required before publishing."
        )

    elif score >= 70:

        remarks = (
            "The EPUB is usable but should be reviewed to improve quality."
        )

    elif score >= 60:

        remarks = (
            "Several quality issues should be fixed before publication."
        )

    else:

        remarks = (
            "This EPUB requires significant improvements before it is ready for release."
        )

    story.append(
        Paragraph(
            remarks,
            styles["NormalText"]
        )
    )

    story.append(Spacer(1,0.5*inch))

    story.append(
        Paragraph(
            "<font color='#5B4BDB'><b>Thank you for using BookForge.</b></font>",
            styles["Heading"]
        )
    )

    add_footer(story, styles)


def generate_pdf(report_data, output_path: Path):
    """
    Generate the complete BookForge PDF report.
    """

    styles = create_styles()

    doc = SimpleDocTemplate(
        str(output_path),
        rightMargin=40,
        leftMargin=40,
        topMargin=40,
        bottomMargin=40
    )

    story = []

    # ----------------------------------
    # Cover Page
    # ----------------------------------
    add_cover_page(
        story,
        styles,
        report_data
    )

    # ----------------------------------
    # Executive Summary
    # ----------------------------------
    add_executive_summary(
        story,
        styles,
        report_data
    )

    add_issues_page(
    story,
    styles,
    report_data
    )

    add_chapter_summary(
    story,
    styles,
    report_data
    )

    add_detailed_chapter_analysis(
        story,
        styles,
        report_data
    )

    add_recommendations_page(
    story,
    styles,
    report_data
    )

    # ----------------------------------
    # Build PDF
    # ----------------------------------
    doc.build(story)

    return output_path