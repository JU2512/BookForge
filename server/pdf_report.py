def collect_unique_issues(report_data):

    grouped = {
        "critical": {},
        "warnings": {},
        "information": {}
    }

    def add_issue(category, issue, chapter=None):

        message = issue.get("message", "").strip()

        if not message:
            return

        if message not in grouped[category]:

            grouped[category][message] = {

                "message": message,

                "severity": issue.get("severity", ""),

                "rule": issue.get(
                    "rule",
                    issue.get("field", "")
                ),

                # Store chapter names instead of count
                "chapters": set()

            }

        # Add chapter name
        if chapter:
            grouped[category][message]["chapters"].add(chapter)

    # ======================================
    # Global Issues (Metadata/CSS/etc.)
    # ======================================

    issues = report_data.get("issues", {})

    for issue in issues.get("critical", []):
        add_issue("critical", issue)

    for issue in issues.get("warnings", []):
        add_issue("warnings", issue)

    for issue in issues.get("information", []):
        add_issue("information", issue)

    # ======================================
    # Chapter Issues
    # ======================================

    for chapter in report_data.get("chapters", []):

        chapter_name = chapter.get(
            "chapter",
            "Unknown"
        )

        for issue in chapter.get("issues", []):

            severity = issue.get(
                "severity",
                "INFO"
            )

            if severity == "ERROR":

                add_issue(
                    "critical",
                    issue,
                    chapter_name
                )

            elif severity == "WARNING":

                add_issue(
                    "warnings",
                    issue,
                    chapter_name
                )

            else:

                add_issue(
                    "information",
                    issue,
                    chapter_name
                )

    # ======================================
    # Convert set → count
    # ======================================

    for category in grouped.values():

        for issue in category.values():

            issue["count"] = len(
                issue["chapters"]
            )

            issue["chapter_list"] = sorted(
                issue["chapters"]
            )

    return grouped

